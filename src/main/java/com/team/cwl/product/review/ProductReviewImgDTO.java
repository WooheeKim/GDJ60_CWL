package com.team.cwl.product.review;

public class ProductReviewImgDTO {
	
	private Long productReviewImgNum;
	private Long productReviewNum;
	private String imgName;
	private String originalName;
	
	public Long getProductReviewImgNum() {
		return productReviewImgNum;
	}
	public void setProductReviewImgNum(Long productReviewImgNum) {
		this.productReviewImgNum = productReviewImgNum;
	}
	public Long getProductReviewNum() {
		return productReviewNum;
	}
	public void setProductReviewNum(Long productReviewNum) {
		this.productReviewNum = productReviewNum;
	}
	public String getImgName() {
		return imgName;
	}
	public void setImgName(String imgName) {
		this.imgName = imgName;
	}
	public String getOriginalName() {
		return originalName;
	}
	public void setOriginalName(String originalName) {
		this.originalName = originalName;
	}
	
}
