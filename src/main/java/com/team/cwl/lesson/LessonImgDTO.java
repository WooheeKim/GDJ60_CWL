package com.team.cwl.lesson;

public class LessonImgDTO {
	
	private Long lessonImgNum;
	private Long lessonNum;
	private String imgName;
	private String originalName;
	
	public Long getLessonImgNum() {
		return lessonImgNum;
	}
	public void setLessonImgNum(Long lessonImgNum) {
		this.lessonImgNum = lessonImgNum;
	}
	public Long getLessonNum() {
		return lessonNum;
	}
	public void setLessonNum(Long lessonNum) {
		this.lessonNum = lessonNum;
	}
	public String getImgName() {
		return imgName;
	}
	public void setImgName(String imgName) {
		this.imgName = imgName;
	}
	public String getOriginalName() {
		return originalName;
	}
	public void setOriginalName(String originalName) {
		this.originalName = originalName;
	}

}
